            <h1>Game-Book Pari-mutuel</h1>

            <p>Our odds are based on parimutuel betting. In traditional gambling sites, known as "bookies", when you place a bet you are betting against the site itself or the "house." The house wants and needs a percentage of people to lose so that they can make money and pay the winners. In our zero-sum system the losers always pay the winners and there is no motive for us to ensure a certain amount of losers. We only take a small commission, we'll try to make the odds the best we can for our users based on bet performance. We truly want everyone to experience winning on our site and wish all players the best of luck. Game-book is part of one of the greatest communities in the world and we want to help it continue to grow and flourish.</p>

            <p>As stated we are a parimutuel platform not a bookie. This means that the way our odds are created are fundamentally different than a bookie whose odds you can determine via the following formula: <br><br><code>(1/decimal odds option A)*100 + (1/decimal odds option B)*100 = some amount above 100.</code></p>

            <p>The number above 100 is how much the bookie is taking.</p>
            
            <p>Bookies takes a percentage of profit off of the odds. However, in our parimutuel system the commission is taken off of the total prize pool and the odds reflect what each side would win if they had a winning bet on that team. The odds listed on our site are just estimates and subject to change whenever bets are place to reflect the new total bet pool and what percentage of the bet pool each team is.</p>
